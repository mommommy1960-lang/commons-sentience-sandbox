def _prepare_synthetic_catalog(
    name: str,
    output_path: Optional[str],
) -> str:
    """Generate a synthetic catalog, write to a temp file if needed, return path."""
    from reality_audit.data_analysis.example_event_data import (
        generate_example_event_dataset,
    )

    if name == "synthetic_isotropic":
        events = generate_example_event_dataset(n=300, seed=42)
    elif name == "synthetic_preferred_axis":
        # Generate isotropic then apply preferred-axis injection
        from reality_audit.data_analysis.simulation_signature_analysis import (
            standardize_events, inject_synthetic_anomaly,
        )
        raw_events = generate_example_event_dataset(n=300, seed=42)
        std_events = standardize_events(raw_events)
        events = inject_synthetic_anomaly(
            std_events,
            anomaly_type="preferred_axis",
            strength=0.6,
            seed=0,
        )
        # Flatten to plain dicts with schema fields only
        events = [_flatten_record(e) for e in events]
    else:
        raise ValueError(f"Unknown synthetic catalog name: {name!r}")

    # If no output_path requested, write to a temp location under data/real/
    if output_path is None:
        os.makedirs(_real_data_dir(), exist_ok=True)
        output_path = os.path.join(_real_data_dir(), f"_synthetic_{name}.csv")

    _write_events_csv(events, output_path)
    return output_path


def _flatten_record(rec: Dict[str, Any]) -> Dict[str, Any]:
    """Strip internal _* fields and return clean schema-keyed dict."""
    return {f: rec.get(f) for f in SCHEMA_FIELDS}


def _read_csv_or_tsv(path: str) -> List[Dict[str, str]]:
    """Read a CSV or TSV file and return list of raw string dicts."""
    # Detect delimiter from file extension or try to sniff
    ext = os.path.splitext(path)[1].lower()
    if ext in (".tsv", ".txt"):
        delimiter = "\t"
    else:
        # Try to sniff
        with open(path, newline="") as f:
            sample = f.read(4096)
        tab_count   = sample.count("\t")
        comma_count = sample.count(",")
        delimiter = "\t" if tab_count > comma_count else ","

    records = []
    with open(path, newline="", encoding="utf-8-sig") as f:
        reader = csv.DictReader(f, delimiter=delimiter)
        for row in reader:
            records.append(dict(row))
    return records


def _normalize_records(
    raw_records: List[Dict[str, str]],
    source_name: str = "unknown",
) -> List[Dict[str, Any]]:
    """Map raw column names to schema fields and coerce numeric types."""
    normalized = []
    for i, raw in enumerate(raw_records):
        rec: Dict[str, Any] = {f: None for f in SCHEMA_FIELDS}

        # Build a lower-case key copy for alias matching
        lc_raw = {k.strip().lower(): v for k, v in raw.items()}

        # Phase 1: direct match (case-insensitive)
        for k, v in lc_raw.items():
            if k in SCHEMA_FIELDS:
                rec[k] = v

        # Phase 2: apply column aliases
        for k, v in lc_raw.items():
            alias_target = _COL_ALIASES.get(k)
            if alias_target and rec.get(alias_target) is None:
                rec[alias_target] = v

        # Phase 3: fallback event_id
        if rec["event_id"] is None or rec["event_id"] == "":
            rec["event_id"] = f"{source_name}_{i:06d}"

        # Phase 4: coerce numerics
        for field in ("energy", "arrival_time", "ra", "dec"):
            val = rec[field]
            if val is not None and val != "":
                try:
                    rec[field] = float(val)
                except (ValueError, TypeError):
                    rec[field] = None
            else:
                rec[field] = None

        # Phase 5: fill instrument / epoch with source name if missing
        if not rec["instrument"]:
            rec["instrument"] = source_name
        if not rec["epoch"]:
            rec["epoch"] = source_name

        # Phase 6: derived galactic proxy
        if rec["dec"] is not None:
            rec["_b_proxy"] = abs(rec["dec"])

        normalized.append(rec)
    return normalized


def _write_events_csv(events: List[Dict[str, Any]], path: str) -> None:
    """Write a list of event dicts to CSV using the schema field order."""
    os.makedirs(os.path.dirname(os.path.abspath(path)), exist_ok=True)
    with open(path, "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=SCHEMA_FIELDS, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(events)
