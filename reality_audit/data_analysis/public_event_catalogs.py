def download_or_prepare_public_catalog(
    name: str,
    output_path: Optional[str] = None,
) -> str:
    """Prepare a public event catalog for use with this pipeline.

    For synthetic built-in catalogs, generates the dataset and writes it to
    disk if ``output_path`` is given.  For real catalogs, returns the path
    to a manually placed file in ``data/real/``.

    Parameters
    ----------
    name : catalog identifier (see module docstring for valid values)
    output_path : if provided, write the prepared catalog CSV here

    Returns
    -------
    Path to the prepared catalog CSV file.

    Notes
    -----
    This function performs NO network requests.  Real catalogs must be
    pre-downloaded and placed in ``data/real/``.  The docstring below
    describes each catalog source.

    Catalog download instructions::

        fermi_lat_grb
            Source: NASA HEASARC Fermi GRB Catalog
            URL: https://heasarc.gsfc.nasa.gov/db-perl/W3Browse/w3table.pl?tablehead=name%3Dfermilgrb
            Fields needed: name, ra_deg, dec_deg, trigger_mjd, instrument
            Export as CSV and save to: data/real/fermi_lat_grb_catalog.csv

        swift_bat3_grb
            Source: Swift BAT3 GRB Catalog (Lien et al. 2016)
            URL: https://swift.gsfc.nasa.gov/results/batgrbcat/
            Fields needed: GRBname, RA, Dec, Trigger_time_MJD
            Export as CSV and save to: data/real/swift_bat3_grb_catalog.csv

        icecube_hese
            Source: IceCube HESE public data release
            URL: https://icecube.wisc.edu/science/data/HE-nu-2010-2012
            Fields needed: event, ra, dec, energy_TeV, year
            Export as CSV and save to: data/real/icecube_hese_events.csv
    """
    if name in ("synthetic_isotropic", "synthetic_preferred_axis"):
        return _prepare_synthetic_catalog(name, output_path)

    # Real catalog: locate in data/real/
    if name in KNOWN_REAL_CATALOGS:
        fname = KNOWN_REAL_CATALOGS[name]
        if fname is None:
            raise ValueError(
                f"Catalog {name!r} requires a manually provided file path. "
                "Use load_public_catalog(path) directly."
            )
        path = os.path.join(_real_data_dir(), fname)
    else:
        # treat as direct path
        path = name

    if not os.path.exists(path):
        raise FileNotFoundError(
            f"Catalog file not found: {path}\n"
            "Real public catalogs must be downloaded manually and placed in data/real/.\n"
            "See data/real/README_public_catalog_ingest.md for instructions."
        )
    return path


def load_public_catalog(name_or_path: str) -> List[Dict[str, Any]]:
    """Load and normalize a public event catalog into the standard schema.

    Parameters
    ----------
    name_or_path : either a built-in catalog name OR a file system path to
                   a CSV/TSV file.  Column names are automatically mapped using
                   ``_COL_ALIASES``.

    Returns
    -------
    List of dicts with keys: event_id, energy, arrival_time, ra, dec,
    instrument, epoch.  Unresolvable fields are set to None.

    Source-specific notes
    ---------------------
    - Fermi-LAT: trigger_mjd → arrival_time; ra_deg → ra; dec_deg → dec
    - Swift BAT3: trigger_time_MJD → arrival_time; fluence (1-150 keV) → energy
    - IceCube HESE: energy_TeV re-labelled as energy; no standard energy unit
      conversion applied here (use energy as a relative field only)
    - Synthetic: generated by example_event_data.py, see that module for schema
    """
    # Built-in synthetic catalogs
    if name_or_path in ("synthetic_isotropic", "synthetic_preferred_axis"):
        path = _prepare_synthetic_catalog(name_or_path, output_path=None)
        raw_records = _read_csv_or_tsv(path)
        return _normalize_records(raw_records, source_name=name_or_path)

    # Otherwise treat as file path
    path = name_or_path

    # Resolve known catalog names to their file paths
    if name_or_path in KNOWN_REAL_CATALOGS:
        fname = KNOWN_REAL_CATALOGS[name_or_path]
        if fname:
            path = os.path.join(_real_data_dir(), fname)

    if not os.path.exists(path):
        raise FileNotFoundError(
            f"Catalog file not found: {path}\n"
            "Place the file in data/real/ and try again.\n"
            "See data/real/README_public_catalog_ingest.md for column-mapping details."
        )

    raw_records = _read_csv_or_tsv(path)
    source_name = os.path.splitext(os.path.basename(path))[0]
    return _normalize_records(raw_records, source_name=source_name)


def describe_catalog_coverage(events: List[Dict[str, Any]]) -> Dict[str, Any]:
    """Summarize catalog coverage and data quality.

    Parameters
    ----------
    events : normalized event list (output of load_public_catalog)

    Returns
    -------
    Dict with keys:
      event_count, available_fields, missingness, ra_dec_coverage,
      time_span_mjd, instrument_labels
    """
    n = len(events)
    if n == 0:
        return {
            "event_count": 0,
            "available_fields": [],
            "missingness": {},
            "ra_dec_coverage": {},
            "time_span_mjd": {},
            "instrument_labels": [],
        }

    # Which SCHEMA_FIELDS are present in any record
    available = [f for f in SCHEMA_FIELDS
                 if any(e.get(f) is not None for e in events)]

    # Missingness: fraction of records where each field is None
    missingness = {}
    for f in SCHEMA_FIELDS:
        missing_count = sum(1 for e in events if e.get(f) is None)
        missingness[f] = round(missing_count / n, 4)

    # RA/Dec coverage
    ras  = [e["ra"]  for e in events if e.get("ra")  is not None]
    decs = [e["dec"] for e in events if e.get("dec") is not None]
    ra_dec_coverage: Dict[str, Any] = {}
    if ras:
        ra_dec_coverage["ra_min"]  = min(ras)
        ra_dec_coverage["ra_max"]  = max(ras)
        ra_dec_coverage["dec_min"] = min(decs) if decs else None
        ra_dec_coverage["dec_max"] = max(decs) if decs else None
        ra_dec_coverage["n_with_position"] = len(ras)
    else:
        ra_dec_coverage["n_with_position"] = 0

    # Time span
    times = [e["arrival_time"] for e in events if e.get("arrival_time") is not None]
    time_span: Dict[str, Any] = {}
    if times:
        time_span["t_min_mjd"] = min(times)
        time_span["t_max_mjd"] = max(times)
        time_span["span_days"] = max(times) - min(times)
        time_span["n_with_time"] = len(times)
    else:
        time_span["n_with_time"] = 0

    # Instrument labels
    instruments = sorted({e.get("instrument") or "unknown" for e in events})

    return {
        "event_count":       n,
        "available_fields":  available,
        "missingness":       missingness,
        "ra_dec_coverage":   ra_dec_coverage,
        "time_span_mjd":     time_span,
        "instrument_labels": instruments,
    }


