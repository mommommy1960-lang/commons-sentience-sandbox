"""
public_event_catalogs.py
========================
Ingestion and normalization layer for public astrophysical event catalogs.

Supported built-in catalog names
---------------------------------
``synthetic_isotropic``
    300-event synthetic isotropic dataset generated by example_event_data.py.
    Suitable for pipeline validation as an isotropic baseline.

``synthetic_preferred_axis``
    300-event synthetic dataset with a preferred-axis anomaly injected at
    strength 0.6.  Suitable for testing anomaly recovery.

``manual_ingest``
    Read a locally placed CSV or TSV file from ``data/real/``.
    See ``data/real/README_public_catalog_ingest.md`` for column-mapping details.

Real public catalog sources (manual placement required)
--------------------------------------------------------
This module does NOT download data automatically.  Real public catalogs from
sources such as NASA/IPAC, the Swift BAT3 GRB catalog, the Fermi-LAT catalog,
or the IceCube HESE event list must be downloaded by the operator and placed in
``data/real/`` before use.  See README_public_catalog_ingest.md for details.

The function ``download_or_prepare_public_catalog`` documents how to obtain
each supported catalog and returns the local file path when available.

Author: commons-sentience-sandbox
"""

