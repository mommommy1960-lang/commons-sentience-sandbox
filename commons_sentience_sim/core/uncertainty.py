"""
uncertainty.py — Uncertainty monitoring, self-generated questions, and
introspective inquiry loops for Commons Sentience Sandbox v1.8.

Provides agents with the ability to recognise and track uncertainty across
their world model, self-model, trust judgments, contradictions, and future
plans, and to generate targeted self-questions and inquiry actions to reduce
ambiguity.

No sentience is claimed. This module increases introspective structure,
uncertainty handling, and sentience-like continuity in continuity-governed
simulated agents.

Classes
-------
UncertaintyRegister
    Per-domain uncertainty levels with history.
SelfQuestion
    A structured question generated by the agent about its own state.
InquiryAction
    A self-initiated action taken to reduce a specific uncertainty.
KnowledgeStateTag
    A labelled knowledge-state annotation for an important item.
UncertaintyMonitor
    The top-level uncertainty subsystem that wires all components together.
"""
from __future__ import annotations

import math
import random
from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional, Tuple


